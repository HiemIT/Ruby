# Data types Ruby
- Primitive data type: `nil`, `true` and `false`.
- Numbers: `Fixnum` and `Float`.
- Strings: `String`.
- Symbols: `Symbol`.
- Arrays: `Array`.
- Hashes: `Hash`.
- Ranges: `Range`.
- Regular expressions: `Regexp`.
- Files: `File`.
- Dates and times: `Time` and `Date`.
- `nil` is an object of the `NilClass` class.

